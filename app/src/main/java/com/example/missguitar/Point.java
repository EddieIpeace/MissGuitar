package com.example.missguitar;

import android.animation.TypeEvaluator;

public class Point {
    private float x;
    private float y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }
    public float GetX() {
        return x;
    }

    public float GetY() {
        return y;
    }
}

